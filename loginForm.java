    private void btnclickActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnclickActionPerformed
        String un,up;
            un=txtusername.getText();
            up=password.getText(); 
             try {
             Class.forName("com.mysql.jdbc.Driver");
            Connection conn=DriverManager.getConnection(
            "jdbc:mysql://localhost:3306/placement","root","Mysql@123");
            
            PreparedStatement ps=conn.prepareStatement("select * from personaldetails where username=? and userpass=?");
            ps.setString(1, un);
            ps.setString(2, up);
            
             ResultSet rs=ps.executeQuery();
            if(rs.next()){
                 MainMenu mn=new MainMenu();
                 mn.setVisible(true);
                 dispose();   
            }
            else{
                JOptionPane.showMessageDialog(rootPane," invalid username or password");
                txtusername.setText(" ");
                password.setText("");
            }
            if(txtusername.equals("")&&password.equals("")){
                ps.clearParameters();
            }
            
            rs.close();
            ps.close();
            conn.close();
        } catch (Exception e) {
            System.out.println(e);
        }
            
            
    }//GEN-LAST:event_btnclickActionPerformed

